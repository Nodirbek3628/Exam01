#  15. **Bo'linuvchanlikni tekshirish**

# **Vazifa:** Foydalanuvchidan son kiritsini so'rang. Shu son:
# - 2 ga bo'linishini
# - 3 ga bo'linishini  
# - 5 ga bo'linishini

number = int(input("Sonni kiritin: "))

if number % 2 == 0:
    print("2 ga bo'linadi")
elif number % 3 == 0:
    print("3 ga bo'linadi")

elif number % 5 == 0:
    print("5 ga bo'linadi")
    